<p>
	This page gives some information about me personally but with a focus on stuff related to
	technology and development.
</p>
<p>
	I was born in 1991 on the little known island nation of <a
		href="https://en.wikipedia.org/wiki/Vanuatu">Vanuatu</a
	> where my dad was doing some contracting work at the time. I moved to New Zealand in 1993 where I
	lived first in Hawkes Bay and later in Stokes Valley, not far from Wellington. I moved to the UK in
	July 2005 where over the last 20 years I have lived in Bromborough, Lancaster, Chester, and Wrexham.
</p>
<p>
	I've been around computers since I was young, initially mainly using them to play games. But as I
	grew up, so did the internet, and I started experimenting with building sites in HTML and adding
	interactivity with JavaScript. These websites initially weren't deployed anywhere and were written
	locally using Windows Notepad. Although this was a good learning experience, something was missing
	and I wondered how the websites I used on a daily basis provided the features that they did. This
	is when I discovered the server side, PHP and began using the humble <a
		href="https://en.wikipedia.org/wiki/LAMP_(software_bundle)">LAMP stack</a
	> which came to dominate the internet for years to come.
</p>

